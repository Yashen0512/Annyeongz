# 合集活動拆成三個面(依來源)
EVENT_FACETS = ['命定站賀文合集', '계간윶녕 : 𝐋𝐎𝐕𝐄 𝐆𝐀𝐌𝐄', '1st鳳梨汁推薦']

def event_group(t):
    if '鳳梨' in t:
        return '1st鳳梨汁推薦'
    if 'LOVE GAME' in t or '계간' in t:
        return '계간윶녕 : 𝐋𝐎𝐕𝐄 𝐆𝐀𝐌𝐄'
    return '命定站賀文合集'

def split_tags(cell):
    cell = (cell or '').strip()
    return [t.strip() for t in cell.split(',') if t.strip()] if cell else []

# 反查:tag -> facet
TAG2FACET = {}
for _fac, _tags in FACETS.items():
    for _t in _tags:
        TAG2FACET[_t] = _fac

def bucketize(tag_cell, no=None):
    """把一篇的標籤格,拆進各分面(含 REMAP、DROP、ADD 校正、合集、去重)。回傳 dict: facet -> [tags]，外加 '合集活動'。"""
    out = OrderedDict((f, []) for f in FACETS)
    for _ef in EVENT_FACETS:
        out[_ef] = []
    def place(tag):
        tag = REMAP.get(tag, tag)
        if tag in DROP:
            return
        if tag in TAG2FACET:
            dst = TAG2FACET[tag]
        elif is_event(tag):
            dst = event_group(tag)
        else:
            return  # 不在任何分面
        if tag not in out[dst]:
            out[dst].append(tag)
    for tag in split_tags(tag_cell):
        place(tag)
    if no is not None and no in ADD:
        for tag in ADD[no]:
            place(tag)
    # 依分面定義的標準順序排,讓每篇標籤顯示順序固定(不受 Notion/API 回傳順序影響)
    for _fac, _tags in FACETS.items():
        _ord = {t: i for i, t in enumerate(_tags)}
        out[_fac].sort(key=lambda t: _ord.get(t, 999))
    return out
